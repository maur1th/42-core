int		ft_compact(char **tab, int length)
{
	int		i;
	int		j;
	int		k;

	i = 0;
	j = 0;
	k = length;
	while (i < length)
	{
		j = 0;
		if (tab[i][j] == '\0')
			k--;
		i++;
	}
	return (k);
}
