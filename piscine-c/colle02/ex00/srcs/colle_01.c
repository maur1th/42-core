#include <colle02.h>

int		colle_01(char *str)
{
	int	i;

	i = 0;
	while (i < 162)
	{
		if ((i % 2 == 0) && (str[i] <= '9' && str[i] > '0'))
			++i;
		else if ((i % 2 == 1) && ((str[i] == ' ') || (str[i] == '\n')))
			++i;
		else
			return (0);
	}
	return (1);
}
