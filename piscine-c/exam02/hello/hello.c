#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *s)
{
	while (*s != '\0')
		ft_putchar(*(s++));
}

int		main(void)
{
	ft_putstr("Hello World!\n");
}
