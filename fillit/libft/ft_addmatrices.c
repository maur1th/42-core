#include "libft.h"

t_matrix	*ft_addmatrices(t_matrix *m1, t_matrix *m2)
{
	char	*s1;
	char	*s2;

	if (m1->size != m2->size)
		return (NULL);
	s1 = m1->data;
	s2 = m2->data;
	while (*s1 != '\0')
		*(s1++) += *(s2++) - '0';
	return (m1);
}
