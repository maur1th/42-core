#include <libft.h>

char	*ft_strrchr(const char *s, int c)
{
	char	*ptr;

	ptr = 0;
	while (*s != '\0')
	{
		if (*s == c)
		{
			ptr = (char*)s;
		}
		s += 1;
	}
	if (c == '\0')
	{
		ptr = (char*)s;
	}
	return (ptr);
}
