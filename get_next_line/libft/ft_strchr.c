#include <libft.h>

char	*ft_strchr(const char *s, int c)
{
	if (s == NULL)
		return (0);
	while (*s != '\0')
	{
		if (*s == c)
			return ((char*)s);
		s += 1;
	}
	if (c == '\0')
		return ((char*)s);
	return (0);
}
