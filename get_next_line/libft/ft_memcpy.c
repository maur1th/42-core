#include <libft.h>

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	void	*dst0;

	dst0 = dst;
	while (n-- != 0)
		*(char*)(dst++) = *(char*)(src++);
	return (dst0);
}
