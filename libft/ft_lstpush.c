#include "libft.h"

void	ft_lstpush(t_list **alst, t_list *new)
{
	if (!(*alst))
		*alst = new;
	else if (!((*alst)->next))
		(*alst)->next = new;
	else
		ft_lstpush(&(*alst)->next, new);
}
